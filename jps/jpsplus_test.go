package jps

import (
	"fmt"
	"image"
	"testing"
)

type graph []string

func (g graph) IsBlock(p image.Point) bool {
	return p.Y < 0 || p.Y >= len(g) || p.X < 0 || p.X >= len(g[p.Y]) || g[p.Y][p.X] != ' '
}

func (g graph) put(p image.Point, c rune) {
	g[p.Y] = g[p.Y][:p.X] + string(c) + g[p.Y][p.X+1:]
}

func (g graph) print() {
	for _, y := range g {
		fmt.Println(y)
	}
}

